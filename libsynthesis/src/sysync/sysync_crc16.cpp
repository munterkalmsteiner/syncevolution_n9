/* compute 16 bit CCITT crc on the fly, doesn't use table */
/* use this one if you are tight on space. */
uInt16 sysync_crc16(uInt16 crc,uInt8 b)
{
  int s;

  /*  s = b ^ (crc & 0xff);*/
  s = (b ^ crc) & 0xff;
  s = s ^ (s << 4);
  s = (crc >> 8) ^ (s << 8) ^ (s << 3) ^ (s >> 4);

  return s & 0xffff;
}
