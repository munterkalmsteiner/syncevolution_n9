/* compute 16 bit CRC using the table */
uInt16 sysync_crc16(uInt16 crc,uInt8 b)
{
  int s, i;

  i = (b ^ crc) & 0xff;
  s = (crc >> 8) ^ crc16_table[i];
  return s & 0xffff;
} // sysync_crc16

