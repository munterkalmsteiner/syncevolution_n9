// calc CRC over a block of bytes
uInt16 sysync_crc16_block(const void* dataP, uInt32 len, uInt16 crc)
{
  uInt32 i;
  uInt8 *p=(uInt8 *)dataP;

  for (i=0; i<len; i++) crc = sysync_crc16(crc,*(p++));
  return crc;
} // sysync_crc16_block

